package Exercícios;

public class Exercício6 {
    public static void main(String[] args) {

        // Declaração das variáveis
        int valor1 = 15;
        int valor2 = 20;

        // Comparações usando operadores de comparação

        System.out.println("valor1 == valor2: " + (valor1 == valor2)); // Igualdade

        System.out.println("valor1 != valor2: " + (valor1 != valor2)); // Diferente

        System.out.println("valor1 > valor2: " + (valor1 > valor2)); // Maior que

        System.out.println("valor1 < valor2: " + (valor1 < valor2)); // Menor que

        System.out.println("valor1 >= valor2: " + (valor1 >= valor2)); // Maior ou igual

        System.out.println("valor1 <= valor2: " + (valor1 <= valor2)); // Menor ou igual
    }

}
